package modelos;

public class VerCali3 {

    int id;
    String materias;
    String periodouno;
    String periododos;
    String periodotres;
    String pediodocuatro;
    String periodocinco;
    String promediofinal;
    String alumno;

    public VerCali3() {

    }

    public VerCali3(int id, String materias, String periodouno, String periododos, String periodotres, String periodocuatro, String periodocinco, String promediofinal, String alumno) {
        this.id = id;
        this.materias = materias;
        this.periodouno = periodouno;
        this.periododos = periododos;
        this.periodotres = periodotres;
        this.pediodocuatro = periodocuatro;
        this.periodocinco = periodocinco;
        this.promediofinal = promediofinal;
        this.alumno = alumno;

    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getMaterias() {
        return materias;
    }

    public void setMaterias(String materias) {
        this.materias = materias;
    }

    public String getPeriodouno() {
        return periodouno;
    }

    public void setPeriodouno(String periodouno) {
        this.periodouno = periodouno;
    }

    public String getPeriododos() {
        return periododos;
    }

    public void setPeriododos(String periododos) {
        this.periododos = periododos;
    }

    public String getPeriodotres() {
        return periodotres;
    }

    public void setPeriodotres(String periodotres) {
        this.periodotres = periodotres;
    }

    public String getPediodocuatro() {
        return pediodocuatro;
    }

    public void setPediodocuatro(String pediodocuatro) {
        this.pediodocuatro = pediodocuatro;
    }

    public String getPeriodocinco() {
        return periodocinco;
    }

    public void setPeriodocinco(String periodocinco) {
        this.periodocinco = periodocinco;
    }

    public String getPromediofinal() {
        return promediofinal;
    }

    public void setPromediofinal(String promediofinal) {
        this.promediofinal = promediofinal;
    }

    public String getAlumno() {
        return alumno;
    }

    public void setAlumno(String alumno) {
        this.alumno = alumno;
    }
}
